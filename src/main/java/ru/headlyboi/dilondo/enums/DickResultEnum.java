package ru.headlyboi.dilondo.enums;

import lombok.AllArgsConstructor;
import lombok.Getter;

/**
 * Перечисление результатов изменения длины пениса в игре "Дилондо".
 * 
 * <p>Этот enum определяет возможные результаты игровой механики
 * изменения длины пениса. Каждый результат имеет соответствующее
 * текстовое представление для отображения пользователю.</p>
 * 
 * <p>Результаты используются для:</p>
 * <ul>
 *   <li>Определения направления изменения длины</li>
 *   <li>Генерации текстовых сообщений для пользователя</li>
 *   <li>Логирования результатов игры</li>
 * </ul>
 */
@Getter
@AllArgsConstructor
public enum DickResultEnum {

    UP("увеличился"),
    DOWN("уменьшился"),
    ;

    private final String result;
}
